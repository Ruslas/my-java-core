package repeat.repeat1;

public class WeekDays {
    public static void main(String[] args) {
        String[] Days = new String[7];
        Days [0] ="Sat";
        Days [1] ="Sun";
        Days [2] ="Mon";
        Days [3] ="Tue";
        Days [4] ="Wen";
        Days [5] ="Th";
        Days [6] ="Fr";
        for (String day: Days) {
            System.out.println(day);
        }
    }
}
